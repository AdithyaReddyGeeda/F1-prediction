def _normalize(s: str) -> str:
    """Normalize for fuzzy match (e.g. São Paulo vs Sao Paulo)."""
    return unicodedata.normalize("NFD", s).encode("ascii", "ignore").decode().lower()


def get_circuit_track_data(
    event_name: str, year: Optional[int] = None, round_number: Optional[int] = None
) -> Optional[dict]:
    """
    Return circuit map data for the given event: center (lat, lon) and optional outline.
    Exact match, then substring match, then normalized (e.g. Emilia-Romagna vs Emilia Romagna).
    """
    if not event_name:
        return None
    name = str(event_name).strip()
    if name in CIRCUIT_GEO:
        return CIRCUIT_GEO[name]
    name_lower = name.lower().replace("-", " ").replace("_", " ")
    for key in CIRCUIT_GEO:
        key_lower = key.lower().replace("-", " ").replace("_", " ")
        if key_lower in name_lower or name_lower in key_lower:
            return CIRCUIT_GEO[key]
    norm_name = _normalize(name)
    for key in CIRCUIT_GEO:
        if _normalize(key) in norm_name or norm_name in _normalize(key):
            return CIRCUIT_GEO[key]
    return None
