def get_circuit_track_data(event_name: str, year: Optional[int] = None, round_number: Optional[int] = None) -> Optional[dict]:
    """
    Return circuit map data for the given event: center (lat, lon) and optional outline.
    Tries exact match on event_name, then substring match. Returns None if not in cache.
    FastF1 could be used here for dynamic circuit info when year/round provided (future).
    """
    if not event_name:
        return None
    name = str(event_name).strip()
    if name in CIRCUIT_GEO:
        return CIRCUIT_GEO[name]
    for key in CIRCUIT_GEO:
        if key.lower() in name.lower() or name.lower() in key.lower():
            return CIRCUIT_GEO[key]
    return None
